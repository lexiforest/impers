import { spawnSync, execFileSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT_DIR = path.resolve(__dirname, "..");
const CONSTANTS_PATH = path.join(ROOT_DIR, "src/ffi/constants.ts");
const DEFAULT_HEADER = path.join(ROOT_DIR, "curl_cffi", "include", "curl", "curl.h");

const CURL_OPT_PREFIX = "CURLOPT_";
const CURL_INFO_PREFIX = "CURLINFO_";

function resolveHeaderPath() {
  if (process.env.CURL_HEADER_PATH) {
    return path.resolve(process.env.CURL_HEADER_PATH);
  }

  if (fs.existsSync(DEFAULT_HEADER)) {
    return DEFAULT_HEADER;
  }

  const curlConfig = findCurlConfigHeader();
  if (curlConfig) {
    return curlConfig;
  }

  throw new Error(
    "Unable to locate curl.h. Set CURL_HEADER_PATH or ensure curl_cffi/include/curl/curl.h exists."
  );
}

function findCurlConfigHeader() {
  try {
    const prefix = execFileSync("curl-config", ["--prefix"], { encoding: "utf8" }).trim();
    if (prefix) {
      const headerPath = path.join(prefix, "include", "curl", "curl.h");
      if (fs.existsSync(headerPath)) {
        return headerPath;
      }
    }
  } catch {
    return null;
  }

  return null;
}

function runPreprocessor(headerPath) {
  const input = `#include "${headerPath}"\n`;
  const candidates = [];

  if (process.env.CC) {
    candidates.push(process.env.CC);
  }

  candidates.push("gcc", "cc", "clang");

  let lastError = "";
  for (const compiler of candidates) {
    const result = spawnSync(compiler, ["-E", "-"], {
      input,
      encoding: "utf8",
    });

    if (result.status === 0) {
      return result.stdout;
    }

    if (result.error) {
      lastError = result.error.message;
    } else if (result.stderr) {
      lastError = result.stderr.toString();
    }
  }

  throw new Error(`Failed to run C preprocessor: ${lastError}`);
}

function stripAttributes(line) {
  return line.replace(/__attribute__\s*\(\([^)]*\)\)/g, "");
}

function stripComments(line) {
  return line.replace(/\/\*.*?\*\//g, "").replace(/\/\/.*$/g, "");
}

function extractEntries(source, prefix) {
  const entries = [];
  const seen = new Set();
  const matcher = new RegExp(`\\b(${prefix}[A-Z0-9_]+)\\s*=\\s*([^,]+)`);

  for (const rawLine of source.split(/\r?\n/)) {
    if (!rawLine.includes(prefix)) {
      continue;
    }

    const cleaned = stripComments(stripAttributes(rawLine)).trim();
    if (!cleaned) {
      continue;
    }

    const match = cleaned.match(matcher);
    if (!match) {
      continue;
    }

    const key = match[1].slice(prefix.length);
    if (seen.has(key)) {
      continue;
    }

    entries.push({ key, value: match[2].trim() });
    seen.add(key);
  }

  return entries;
}

function renderObject(name, entries) {
  if (!entries.length) {
    throw new Error(`No entries found for ${name}`);
  }

  const output = [];
  output.push(`export const ${name} = {`);
  for (const entry of entries) {
    output.push(`  ${entry.key}: ${entry.value},`);
  }
  output.push("} as const;");
  output.push("");
  output.push(`export type ${name} = (typeof ${name})[keyof typeof ${name}];`);

  return output.join("\n");
}

function replaceSection(source, startMarker, endMarker, replacement) {
  const startIndex = source.indexOf(startMarker);
  if (startIndex === -1) {
    throw new Error(`Missing start marker: ${startMarker}`);
  }

  const endIndex = source.indexOf(endMarker);
  if (endIndex === -1) {
    throw new Error(`Missing end marker: ${endMarker}`);
  }

  if (endIndex < startIndex) {
    throw new Error(`End marker appears before start marker: ${endMarker}`);
  }

  const before = source.slice(0, startIndex + startMarker.length);
  const after = source.slice(endIndex);
  return `${before}\n${replacement}\n${after}`;
}

const headerPath = resolveHeaderPath();
const preprocessed = runPreprocessor(headerPath);
const curlOptEntries = extractEntries(preprocessed, CURL_OPT_PREFIX);
const curlInfoEntries = extractEntries(preprocessed, CURL_INFO_PREFIX);

const optSection = renderObject("CurlOpt", curlOptEntries);
const infoSection = renderObject("CurlInfo", curlInfoEntries);

const optReplacement = [
  "// This section is generated by scripts/generate-ffi-constants.js. Do not edit by hand.",
  optSection,
].join("\n");

const infoReplacement = [
  "// This section is generated by scripts/generate-ffi-constants.js. Do not edit by hand.",
  infoSection,
].join("\n");

const source = fs.readFileSync(CONSTANTS_PATH, "utf8");
const withCurlOpt = replaceSection(
  source,
  "// BEGIN GENERATED CURL OPTIONS",
  "// END GENERATED CURL OPTIONS",
  optReplacement
);

const withCurlInfo = replaceSection(
  withCurlOpt,
  "// BEGIN GENERATED CURL INFO",
  "// END GENERATED CURL INFO",
  infoReplacement
);

fs.writeFileSync(CONSTANTS_PATH, withCurlInfo);
